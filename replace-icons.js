const htmlPath = path.join(__dirname, 'example.html');
let html = fs.readFileSync(htmlPath, 'utf8');

console.log('开始替换 HTML 中的 emoji 图标...');

// 替换所有匹配的 emoji 为 SVG
let replacementCount = 0;
for (const [emoji, iconName] of Object.entries(iconMap)) {
    const icon = icons[iconName];
    if (icon) {
        const regex = new RegExp(emoji.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
        const matches = html.match(regex);
        if (matches) {
            replacementCount += matches.length;
            html = html.replace(regex, icon);
            console.log(`替换 ${emoji} -> ${iconName} (${matches.length} 处)`);
        }
    }
}

// 保存修改后的 HTML
fs.writeFileSync(htmlPath, html, 'utf8');

console.log(`\n完成！共替换了 ${replacementCount} 处图标。`);
